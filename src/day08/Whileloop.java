package day08;

public class Whileloop {

	public static void main(String[] args) {
		
       int a=1;
		
		while(a <=5)
		{
		System.out.println("Good day..!");
		a=a+1;
		}
		
		
		int b=5;
		while(b>=1)
		{
			System.out.println("Java");
			b=b-1;
		}
		
		
		int c=-1;
		while(c>=-5)
		{
			System.out.println("Python");
			c=c-1;
		}
		
		
		double d=0.1;
		while(d<=0.5)
		{
			System.out.println("Selenium");
			d=d+0.1;
		}
		
	}


	}


